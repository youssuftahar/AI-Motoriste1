import numpy as np
import heapq

# ===========================
# مشروع AI Motoriste
# بقلم: يوسف
# سيارة ذكية بخوارزمية A*
# ===========================

حجم = 10
مدينة_أساس = np.zeros((حجم,حجم), dtype=int)
للحواجز = [(1,1),(1,3),(1,7),(2,5),(3,2),(3,6),
           (4,4),(5,7),(6,3),(7,2),(7,6),(8,7)]
for h in للحواجز:
    مدينة_أساس[h] = 1

def ابحث(مدينة, بداية, هدف):
    def h(n): return abs(n[0]-هدف[0]) + abs(n[1]-هدف[1])
    طابور = [(h(بداية), 0, بداية, [بداية])]
    زيارات = set()
    while طابور:
        _, تكلفة, موقع, مسار = heapq.heappop(طابور)
        if موقع in زيارات: continue
        زيارات.add(موقع)
        if موقع == هدف: return مسار
        for dx,dy in [(0,1),(1,0),(0,-1),(-1,0)]:
            x2,y2 = موقع[0]+dx, موقع[1]+dy
            if 0<=x2<حجم and 0<=y2<حجم and مدينة[x2][y2]==0 and (x2,y2) not in زيارات:
                heapq.heappush(طابور, (تكلفة+1+h((x2,y2)), تكلفة+1, (x2,y2), مسار+[(x2,y2)]))
    return None

هدف = (9,9)
حاجز_متحرك = [2,0]
اتجاه = 1
موقع_سيارة = (0,0)

print("🚗 AI Motoriste - سيارة ذكية بـ A*")
print("="*40)

for لحظة in range(30):
    مدينة = مدينة_أساس.copy()
    مدينة[حاجز_متحرك[0]][حاجز_متحرك[1]] = 1
    
    مسار = ابحث(مدينة, موقع_سيارة, هدف)
    
    if مسار and len(مسار) > 1:
        موقع_سيارة = مسار[1]
    
    خريطة = [['⬜' if مدينة[i][j]==0 else '🟫' 
              for j in range(حجم)] for i in range(حجم)]
    خريطة[موقع_سيارة[0]][موقع_سيارة[1]] = '🚗'
    خريطة[هدف[0]][هدف[1]] = '🏁'
    خريطة[حاجز_متحرك[0]][حاجز_متحرك[1]] = '🔴'
    
    print(f"\nخطوة {لحظة+1}:")
    for صف in خريطة:
        print(' '.join(صف))
    
    if موقع_سيارة == هدف:
        print(f"\n🎉 وصلت في {لحظة+1} خطوة!")
        break
    
    حاجز_متحرك[0] += اتجاه
    if حاجز_متحرك[0] >= 8 or حاجز_متحرك[0] <= 1:
        اتجاه *= -1
