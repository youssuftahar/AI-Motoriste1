import numpy as np
import heapq
import matplotlib.pyplot as plt
import matplotlib.animation as animation
from IPython.display import HTML

حجم = 10
مدينة = np.zeros((حجم,حجم), dtype=int)
للحواجز = [(1,1),(1,3),(1,7),(2,5),(3,2),(3,6),
           (4,4),(5,7),(6,3),(7,2),(7,6),(8,7)]
for h in للحواجز:
    مدينة[h] = 1

def ابحث(مدينة, بداية, هدف):
    def h(n): return abs(n[0]-هدف[0]) + abs(n[1]-هدف[1])
    طابور = [(h(بداية), 0, بداية, [بداية])]
    زيارات = set()
    while طابور:
        _, تكلفة, موقع, مسار = heapq.heappop(طابور)
        if موقع in زيارات: continue
        زيارات.add(موقع)
        if موقع == هدف: return مسار
        for dx,dy in [(0,1),(1,0),(0,-1),(-1,0)]:
            x2,y2 = موقع[0]+dx, موقع[1]+dy
            if 0<=x2<حجم and 0<=y2<حجم and مدينة[x2][y2]==0 and (x2,y2) not in زيارات:
                heapq.heappush(طابور, (تكلفة+1+h((x2,y2)), تكلفة+1, (x2,y2), مسار+[(x2,y2)]))
    return None

# إعداد الرسم
fig, ax = plt.subplots(1,1, figsize=(8,8))

هدف = (9,9)
حاجز_متحرك = [2,0]
اتجاه = [1]
موقع_سيارة = [(0,0)]
مسارات = [[]]

def رسم_إطار(frame):
    مدينة_حالية = مدينة.copy()
    مدينة_حالية[حاجز_متحرك[0]][حاجز_متحرك[1]] = 1
    
    مسار = ابحث(مدينة_حالية, موقع_سيارة[0], هدف)
    if مسار and len(مسار) > 1:
        موقع_سيارة[0] = مسار[1]
        مسارات[0] = مسار
    
    # رسم الخريطة
    خريطة_ألوان = np.zeros((حجم, حجم, 3))
    
    for i in range(حجم):
        for j in range(حجم):
            if مدينة[i][j] == 1:
                خريطة_ألوان[i][j] = [0.4, 0.2, 0.0]  # بني - حاجز
            else:
                خريطة_ألوان[i][j] = [0.9, 0.9, 0.9]  # رمادي - طريق
    
    # المسار
    if مسارات[0]:
        for x,y in مسارات[0]:
            خريطة_ألوان[x][y] = [0.5, 0.8, 1.0]  # أزرق فاتح
    
    # الهدف
    خريطة_ألوان[هدف[0]][هدف[1]] = [0.0, 0.8, 0.0]  # أخضر
    
    # الحاجز المتحرك
    خريطة_ألوان[حاجز_متحرك[0]][حاجز_متحرك[1]] = [1.0, 0.0, 0.0]  # أحمر
    
    # السيارة
    x,y = موقع_سيارة[0]
    خريطة_ألوان[x][y] = [1.0, 0.8, 0.0]  # أصفر
    
    ax.clear()
    ax.imshow(خريطة_ألوان)
    ax.set_title(f'🚗 AI Motoriste - الخطوة {frame+1}', fontsize=14)
    ax.axis('off')
    
    # تحريك الحاجز
    حاجز_متحرك[0] += اتجاه[0]
    if حاجز_متحرك[0] >= 8 or حاجز_متحرك[0] <= 1:
        اتجاه[0] *= -1
    
    if موقع_سيارة[0] == هدف:
        ax.set_title('🎉 وصلت!', fontsize=16)

ani = animation.FuncAnimation(fig, رسم_إطار, frames=25, interval=500)
HTML(ani.to_jshtml())
